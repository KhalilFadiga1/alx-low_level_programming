#include <stdio.h>

/**
 * add - Add two numbers
 * @x: First integer
 * @y: Second Integer
 * Return: Sum of the integers
 */
int add(int x, int y)
{
	return (x + y);
}

/**
 * sub - Find the diff between two numbers
 * @x: First integer
 * @y: Second Integer
 * Return: Difference of the integers
 */
int sub(int x, int y)
{
        return (x - y);
}

/**
 * mul - Product of two numbers
 * @x: First integer
 * @y: Second Integer
 * Return: Product of the integers
 */
int mul(int x, int y)
{
        return (x * y);
}

/**
 * div - Divide two numbers
 * @x: First integer
 * @y: Second Integer
 * Return: Result of the division
 */
int div(int x, int y)
{
        return (x / y);
}

/**
 * mod - Find modulo of two numbers
 * @x: First integer
 * @y: Second Integer
 * Return: remainder of the modulo
 */
int mod(int x, int y)
{       
        return (x % y);
}       
